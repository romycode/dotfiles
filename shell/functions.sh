#!/usr/bin/env sh

git_clean() {
    git branch --merged | grep -v '\*\|master\|main' | xargs -n 1 git branch -d
}

nvm() {
    unset -f nvm
    export NVM_DIR=~/.nvm
    [ -s "$NVM_DIR/nvm.sh" ] && . "$NVM_DIR/nvm.sh"  # This loads nvm
    nvm "$@"
}
 
